package dto;

import java.util.Date;

/**
 *
 * @author Dell
 */
public class ExpenseDTO {
    private int id;
    private Date date;
    private double amount;
    private String content;

    public ExpenseDTO() {
    }

    public ExpenseDTO(int id, Date date, double amount, String content) {
        this.id = id;
        this.date = date;
        this.amount = amount;
        this.content = content;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public double getAmount() {
        return amount;
    }

    public void setAmount(double amount) {
        this.amount = amount;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
    
}
